# Строки (Strings)
# Строки в Python задаются одним из следующих образов:
s = "Python"
s = 'Python'
s = """Python"""
s = '''Python'''

# Многострочные строки можно задавать следующим образом:
multiline_string = """first
second
third"""

multiline_string = "first\nsecond\nthird"

multiline_string = "first\n" \
                   "second\n" \
                   "third"